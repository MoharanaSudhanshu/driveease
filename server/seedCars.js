const seedCars = async () => {
  try {
    await Car.bulkWrite(
      cars.map((car) => ({
        updateOne: {
          filter: {
            make: car.make,
            model: car.model,
            year: car.year,
          },
          update: {
            $set: car,
          },
          upsert: true,
        },
      })),
    );

    console.log("Cars Seeded Successfully");

    process.exit();
  } catch (error) {
    console.log(error);
    process.exit(1);
  }
};


seedCars();
